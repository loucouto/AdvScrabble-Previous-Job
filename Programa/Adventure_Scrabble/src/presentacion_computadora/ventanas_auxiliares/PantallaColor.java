    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        // TODO add your handling code here:
       try
        {
            JFileChooser fc = new JFileChooser();
            fc.setCurrentDirectory(new java.io.File("C:\\Users\\Lourdes"));
            fc.setDialogTitle("Seleccione una ubicacion");
            
            if(fc.showOpenDialog(this.btnSeleccionar) == JFileChooser.APPROVE_OPTION)
            {
               if(fc.getSelectedFile().getAbsolutePath().endsWith(".jpg") || fc.getSelectedFile().getAbsolutePath().endsWith(".png") || fc.getSelectedFile().getAbsolutePath().endsWith(".bmp") || fc.getSelectedFile().getAbsolutePath().endsWith(".gif"))
               {
                   this.txtDirectorio.setText(fc.getSelectedFile().getAbsolutePath());
               
                   File miimagen = new File(fc.getSelectedFile().getAbsolutePath());
                   imagen = ImageIO.read(miimagen);
                   lblImagen.setIcon(new javax.swing.ImageIcon(imagen));
                   archivo = fc.getSelectedFile().getPath();
                   this.lblMensaje.setText("");
               }
               else
               {
                   this.lblMensaje.setForeground(Color.RED);
                   this.lblMensaje.setText("Debe elegir archivos con las siguientes extensiones: jpg, png, bmp o gif");
               }
            }
            else
            {
                 this.lblMensaje.setForeground(Color.BLACK);
                 this.lblMensaje.setText("Operacion Cancelada");
            
            }
        }
        catch(Exception ex)
        {
            this.lblMensaje.setForeground(Color.RED);
            this.lblMensaje.setText(ex.toString());
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed

