    private void txtTamanioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTamanioKeyReleased
        // TODO add your handling code here:
        try
        {
            if((Integer.parseInt(this.txtTamanio.getText())>0) && (this.txtTamanio.getText() != ""))
            {
                this.EstablecerVistaPrevia(); 
            }
          
        }
        catch(Exception ex)
        {
             if((Integer.parseInt(this.txtTamanio.getText())<0) || (this.txtTamanio.getText() == ""))
             {
                 this.lblError.setText("Debe ingresar un valor no nulo");
             }
             else
             {
                this.lblError.setText(ex.toString());
             }
        }
        
        
    }//GEN-LAST:event_txtTamanioKeyReleased

    private void txtTamanioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTamanioKeyTyped
        // TODO add your handling code here:
        try
        {
              char caracter = evt.getKeyChar(); 

              if(Character.isLetter(caracter)) 
              { 
                  getToolkit().beep(); 
                  evt.consume(); 
                  this.lblError.setText("Ingrese sólamente números"); 
                  //this.lblError.setText("" + evt.getKeyCode()); 
              }
              else
              {
                  this.lblError.setText(""); 
              }
        }
        catch(Exception ex)
        {
             this.lblError.setText(ex.toString());
        }
    }//GEN-LAST:event_txtTamanioKeyTyped

