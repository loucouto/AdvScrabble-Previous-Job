    private void CargarDatos()
    {
         lista_fuentes = GraphicsEnvironment.getLocalGraphicsEnvironment().getAvailableFontFamilyNames();
            
           for(String s: lista_fuentes)
           {
               modelo.addElement(s);
           }
           
           this.lstFuentes.setModel(modelo);
           this.lstFuentes.setSelectedValue("Ubuntu Light", true);
           this.lstEstilos.setSelectedIndex(1);
           this.lstTamanios.setSelectedValue("16", true);
         
           this.txtFuente.setText("Ubuntu Light");
           this.txtTamanio.setText("16");
           this.txtEstilo.setText("Bold");
           
           fuente = new Font("Ubuntu Light", 1, 16);
           this.txtVistaPrevia.setFont(fuente);
    }
    
    private void EstablecerVistaPrevia()
    {       
                fuente = new Font(this.txtFuente.getText(), this.lstEstilos.getSelectedIndex(), Integer.parseInt(this.txtTamanio.getText()));
                this.txtVistaPrevia.setFont(fuente);          
    }
    
