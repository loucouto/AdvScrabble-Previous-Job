    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        try
        {
            String titulo = "";
            String descripcion = "";
            Date fecha_hecho = new Date();
            Date fecha_baja = new Date();
            Funcionalidad f = new Funcionalidad();
            ArrayList<SubFuncionalidad> lista_sub_funcionalidad = new ArrayList(); 
            boolean estaVacio = false;
            int reintegro = 0;
            String mensaje_advertencia = "";
            
          //  DefaultTableModel modelo = new DefaultTableModel();
            
              if(this.chcHaySubFuncionalidad.isSelected() == false)
              {
                for(int i = 0; (i< this.tblSubFuncionalidad.getRowCount() && estaVacio == false); i++)
                {           
                      if(this.tblSubFuncionalidad.getValueAt(i, 1).equals(""))
                      {
                          estaVacio = true;
                      }
                }
              }
         
            if(this.txtTitulo.getText().equals(""))
            {
                this.lblMensaje.setText("El campo Título no puede estar vacío");
                this.lblMensaje.setForeground(Color.RED);
            }
            else if(estaVacio == true && this.chcHaySubFuncionalidad.isSelected() == false )
            {
               this.lblMensaje.setText("El campo Título no puede estar vacío para todas las celdas de la tabla de Subfuncionalidades");
               this.lblMensaje.setForeground(Color.RED);
            }
            else
            {
                      id_funcionalidad = Integer.parseInt(this.txtIdFuncionalidad.getText());  
                      titulo = this.txtTitulo.getText();
                      descripcion = this.txtDescripcion.getText();
                      fecha_hecho = this.clnFechaAlta.getDate();
                      fecha_baja = this.clnFechaBaja.getDate();
                     
                      if(this.chcHaySubFuncionalidad.isSelected() == false)
                      {
                            for(int i = 0; i< this.tblSubFuncionalidad.getRowCount(); i++)
                            {
                                SubFuncionalidad sf = new SubFuncionalidad();

                                sf.setId((int)this.tblSubFuncionalidad.getValueAt(i, 0));
                                sf.setTitulo((String)this.tblSubFuncionalidad.getValueAt(i, 1));
                                sf.setDescripcion((String)this.tblSubFuncionalidad.getValueAt(i,2));
                                sf.setFecha_hecho((Date)this.tblSubFuncionalidad.getValueAt(i, 3));

                                lista_sub_funcionalidad.add(sf);
                            }
                      }
                            f = new Funcionalidad(id_funcionalidad, titulo, descripcion, fecha_hecho, fecha_baja, lista_sub_funcionalidad); 
                      
                switch(this.cmbAccion.getSelectedIndex())
                {
                    case 0:
                  
                       if(FabricaLogica.getControladorFuncionalidad().AltaFuncionalidad(f)>0)
                       {
                           this.lblMensaje.setText("Alta de Funcionalidad exitosa");
                           this.lblMensaje.setForeground(Color.BLACK);
                           this.RecargarValores(0);
                       }
                       else
                       {
                           this.lblMensaje.setText("Alta de Funcionalidad NO exitosa");
                           this.lblMensaje.setForeground(Color.RED);
                       }
                       
                    break;
                  
                    case 1:
               
                    mensaje_advertencia = "Usted al realizar una modificación va a sobrescribir los datos ya existentes del IdFuncionalidad: " + id_funcionalidad + " ¿Está seguro que desea modificar este registro?";
                
                    if(JOptionPane.showConfirmDialog(this, mensaje_advertencia, "Mensaje de Advertencia" , 0, 2) == 0)
                    {
                           if(FabricaLogica.getControladorFuncionalidad().ModificarFuncionalidad(f)>0)
                           {
                               this.lblMensaje.setText("Modificación de Funcionalidad exitosa");
                               this.lblMensaje.setForeground(Color.BLACK);
                               this.RecargarValores(1);
                           }
                           else
                           {
                               this.lblMensaje.setText("Modificación de Funcionalidad NO exitosa");
                               this.lblMensaje.setForeground(Color.RED);
                           }

                    }      
                    break;
                   
                    case 2:
                      
                    mensaje_advertencia = "Usted al realizar una baja va a eliminar los datos ya existentes del IdFuncionalidad: " + id_funcionalidad + " ¿Está seguro que desea dar de baja este registro?";
                
                    if(JOptionPane.showConfirmDialog(this, mensaje_advertencia, "Mensaje de Advertencia" , 0, 2) == 0)
                    {   
                       if(FabricaLogica.getControladorFuncionalidad().BajaFuncionalidad(f)>0)
                       {
                           this.lblMensaje.setText("Baja de Funcionalidad exitosa");
                           this.lblMensaje.setForeground(Color.BLACK);
                           this.RecargarValores(2);
                       }
                       else
                       {
                           this.lblMensaje.setText("Baja de Funcionalidad NO exitosa");
                           this.lblMensaje.setForeground(Color.RED);
                       }
                    }
                    break;
                    case 3:
                        
                       if(FabricaLogica.getControladorFuncionalidad().ReingresarFuncionalidad(f)>0)
                       {
                           this.lblMensaje.setText("Reingreso de Funcionalidad exitosa");
                           this.lblMensaje.setForeground(Color.BLACK);
                           this.RecargarValores(3);
                       }
                       else
                       {
                           this.lblMensaje.setText("Reingreso de Funcionalidad NO exitosa");
                           this.lblMensaje.setForeground(Color.RED);
                       }

                    break;
                    default:
                        
                        for(SubFuncionalidad sf : lista_sub_funcionalidad)
                        {
                            reintegro = FabricaLogica.getControladorSubFuncionalidad().ReingresarSubFuncionalidad(id_funcionalidad, sf);
                        }
                        
                        if(reintegro>0)
                       {
                           this.lblMensaje.setText("Reingreso de la lista de Sub-Funcionalidad exitoso");
                           this.lblMensaje.setForeground(Color.BLACK);
                           this.RecargarValores(4);
                       }
                       else
                       {
                           this.lblMensaje.setText("Reingreso de la lista de Sub-Funcionalidad NO exitoso");
                           this.lblMensaje.setForeground(Color.RED);
                       }
 
                      
                }
            }
        }
        catch(Exception ex)
        {
            this.lblMensaje.setForeground(Color.RED);
            this.lblMensaje.setText(ex.toString());
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

