    private void tblSubFuncionalidadKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblSubFuncionalidadKeyReleased
        // TODO add your handling code here:
        try
        {
            if(evt.getKeyCode() == 10)
            {
              id_subfuncionalidad++;
              Object[] sf_vector = {id_subfuncionalidad, "","",clnFechaAlta.getDate()};
              modelo_tabla.addRow(sf_vector);
              this.tblSubFuncionalidad.setModel(modelo_tabla);
            }
            else if(evt.getKeyCode() == 17)
            {
                modelo_tabla.removeRow(this.tblSubFuncionalidad.getSelectedRow());
                if(cmbAccion.getSelectedIndex() == 0)
                {
                    id_subfuncionalidad--;

                    for(int i=0; i<id_subfuncionalidad; i++)
                    {
                        modelo_tabla.setValueAt(i + 1, i, 0);
                    }
                }
                this.tblSubFuncionalidad.setModel(modelo_tabla);
            }
        }
        catch(Exception ex)
        {
            this.lblMensaje.setText(ex.toString());
        }        
    }//GEN-LAST:event_tblSubFuncionalidadKeyReleased

