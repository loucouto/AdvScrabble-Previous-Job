    private void BuscarFuncionalidad()
    {
         Funcionalidad f = new Funcionalidad();
        
            if(this.txtIdFuncionalidad.getText().equals("") == false)
            {
                  this.id_funcionalidad = Integer.parseInt(this.txtIdFuncionalidad.getText());
                  if(this.cmbAccion.getSelectedIndex() == 1 || this.cmbAccion.getSelectedIndex() == 2)
                  {
                     f = FabricaLogica.getControladorFuncionalidad().BuscarFuncionalidadPorId(this.id_funcionalidad);
                  }
                  else if(this.cmbAccion.getSelectedIndex() == 4)
                  {
                      f = FabricaLogica.getControladorFuncionalidad().BuscarFuncionalidadConSubFuncionalidadEliminados(id_funcionalidad);
                  
                  }
                  else
                  {
                     f = FabricaLogica.getControladorFuncionalidad().BuscarFuncionalidadEliminadaPorId(id_funcionalidad);
                  }
                  
                  if(f!=null)
                  {
                  
                      this.txtTitulo.setText(f.getTitulo());
                      this.txtDescripcion.setText(f.getDescripcion());
                      this.clnFechaAlta.setDate(f.getFecha_hecho());
                      
                      modelo_tabla = new DefaultTableModel();
                      
                      if(this.cmbAccion.getSelectedIndex() == 1 || this.cmbAccion.getSelectedIndex() == 2)
                      {
                         this.lblFuncionalidadEliminada.setVisible(false);
                         this.lblFuncionalidadEliminada.setText("");
                         this.txtTitulo.setForeground(Color.BLACK);
                         this.txtDescripcion.setForeground(Color.BLACK);
                         this.tblSubFuncionalidad.setForeground(Color.BLACK);
                         this.clnFechaAlta.setForeground(Color.BLACK);
                      
                          Object[] columna_tabla = {"Id_SubFuncionalidad", "Titulo","Descripcion","Fecha de Alta"};
                          modelo_tabla.setColumnIdentifiers(columna_tabla);
                          for(SubFuncionalidad sf : f.getSub_funcionalidad())
                          {
                               Object[] vector ={sf.getId(), sf.getTitulo(), sf.getDescripcion(), sf.getFecha_hecho()};
                               modelo_tabla.addRow(vector);
                               id_subfuncionalidad = sf.getId();
                          }
                          if(this.cmbAccion.getSelectedIndex() == 1)
                          {
                               this.txtTitulo.setEnabled(true);
                               this.txtDescripcion.setEnabled(true);
                               this.chcHaySubFuncionalidad.setEnabled(true);
                               this.chcHaySubFuncionalidad.setSelected(false);
                               this.tblSubFuncionalidad.setEnabled(true);
                               this.clnFechaAlta.setEnabled(true);
                          }
                          else
                          { 
                               this.clnFechaBaja.setEnabled(true);
                          }
                      
                      }
                      else
                      {
                            Object[] columna_tabla = {"Id_SubFuncionalidad", "Titulo","Descripcion","Fecha de Alta","Fecha de Baja"};
                            modelo_tabla.setColumnIdentifiers(columna_tabla);
                            for(SubFuncionalidad sf : f.getSub_funcionalidad())
                            {
                                 Object[] vector ={sf.getId(), sf.getTitulo(), sf.getDescripcion(), sf.getFecha_hecho(), sf.getFecha_baja()};
                                 modelo_tabla.addRow(vector);
                            }
                               this.tblSubFuncionalidad.setForeground(Color.RED);

                             if(this.cmbAccion.getSelectedIndex() == 3)
                             {
                               this.lblFuncionalidadEliminada.setVisible(true);
                               this.lblFuncionalidadEliminada.setText("ELIMINAD0");
                               this.txtTitulo.setForeground(Color.RED);
                               this.txtDescripcion.setForeground(Color.RED);
                               this.clnFechaAlta.setForeground(Color.RED);
                             }
                      }
                        this.tblSubFuncionalidad.setModel(modelo_tabla);
                        this.btnAceptar.setEnabled(true);
                 }
                 else
                 {
                      this.txtTitulo.setText("");
                      this.txtDescripcion.setText("");
                      this.clnFechaAlta.setDate(new Date());
                      
                      modelo_tabla = new DefaultTableModel();
                      Object[] columna_tabla = {"Id_SubFuncionalidad", "Titulo","Descripcion","Fecha de Alta"};
                      this.tblSubFuncionalidad.setModel(modelo_tabla);
                      
                          this.txtTitulo.setEnabled(false);
                          this.txtDescripcion.setEnabled(false);
                          this.clnFechaAlta.setEnabled(false);
                          this.clnFechaBaja.setEnabled(false);
                          this.btnAceptar.setEnabled(false);
                      
                      this.lblMensaje.setText("No existe una funcionalidad para el Id ingresado. Prueba con otro Id.");
                 }
           }
    }
    
